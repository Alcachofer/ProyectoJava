    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        try {
            int confirmacion = JOptionPane.showConfirmDialog(this, "Desea eliminar al cliente: "+Clientes.getClientes().get(lClientes.getSelectedIndex()).getNombre()+"?");
            if(JOptionPane.OK_OPTION == confirmacion){
                Clientes.removeCliente(Clientes.getClientes().get(lClientes.getSelectedIndex()));
                load();
            }
            }catch(RuntimeException e){
                JOptionPane.showMessageDialog(this, "Selecione un cliente");
            }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

