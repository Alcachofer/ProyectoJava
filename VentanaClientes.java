    private void lnBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lnBuscarKeyReleased
        DefaultListModel model = new DefaultListModel();
        for (Cliente cliente : Clientes.search(lnBuscar.getText())) {
                model.addElement(cliente);
        }
        lClientes.setModel(model);
    }//GEN-LAST:event_lnBuscarKeyReleased

