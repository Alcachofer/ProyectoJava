    private void load(){
        DefaultListModel model = new DefaultListModel();
        lblAuto.setText(auto.getMarca()+" ( "+auto.getModelo()+" )");     
        for (Arreglo arreglo : auto.getArreglos()) {
            model.addElement(arreglo);        
        }
        lArreglos.setModel(model);
    }
