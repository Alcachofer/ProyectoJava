    private void btnRemoveArregloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveArregloActionPerformed
        try {
            int confirmacion = JOptionPane.showConfirmDialog(this, "Desea eliminar el arreglo seleccionado ?");
            if (JOptionPane.OK_OPTION == confirmacion){
                auto.removeArreglo(auto.getArreglos().get(lArreglos.getSelectedIndex()));
                load();
            }
        }catch(RuntimeException e){
            JOptionPane.showMessageDialog(this, "Seleccione un arreglo");
        }
    }//GEN-LAST:event_btnRemoveArregloActionPerformed


